const testing = std.testing;

fn has(argv: []const []const u8, needle: []const u8) bool {
    for (argv) |a| if (std.mem.eql(u8, a, needle)) return true;
    return false;
}

fn hasPrefix(argv: []const []const u8, prefix: []const u8) bool {
    for (argv) |a| if (std.mem.startsWith(u8, a, prefix)) return true;
    return false;
}

test "build emits the critical flags incl. bt-detach-seed-only" {
    const argv = try build(testing.allocator, .{
        .port = 6810,
        .secret = "abc123",
        .dir = "/tmp/dl",
        .seed_ratio = 5.0,
    });
    defer free(testing.allocator, argv);

    try testing.expectEqualStrings("aria2c", argv[0]);
    try testing.expect(has(argv, "--enable-rpc=true"));
    try testing.expect(has(argv, "--bt-detach-seed-only=true"));
    try testing.expect(has(argv, "--rpc-listen-port=6810"));
    try testing.expect(has(argv, "--rpc-secret=abc123"));
    try testing.expect(has(argv, "--dir=/tmp/dl"));
    try testing.expect(has(argv, "--seed-ratio=5.00"));
    try testing.expect(has(argv, "--max-concurrent-downloads=32"));
}

test "seed-ratio floored at 1.0; seed-time omitted when zero" {
    const argv = try build(testing.allocator, .{
        .port = 1,
        .secret = "s",
        .dir = "/d",
        .seed_ratio = 0.1,
        .seed_time_min = 0,
    });
    defer free(testing.allocator, argv);
    try testing.expect(has(argv, "--seed-ratio=1.00"));
    try testing.expect(!hasPrefix(argv, "--seed-time="));
}

test "session flags only when paths supplied" {
    const none = try build(testing.allocator, .{ .port = 1, .secret = "s", .dir = "/d" });
    defer free(testing.allocator, none);
    try testing.expect(!hasPrefix(none, "--save-session="));
    try testing.expect(!hasPrefix(none, "--input-file="));

    const both = try build(testing.allocator, .{
        .port = 1,
        .secret = "s",
        .dir = "/d",
        .session_save = "/d/sess",
        .session_load = "/d/sess",
        .seed_time_min = 60,
    });
    defer free(testing.allocator, both);
    try testing.expect(has(both, "--save-session=/d/sess"));
    try testing.expect(has(both, "--save-session-interval=30"));
    try testing.expect(has(both, "--input-file=/d/sess"));
    try testing.expect(has(both, "--seed-time=60"));
}
